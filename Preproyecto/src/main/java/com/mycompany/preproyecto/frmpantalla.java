        EntityManagerFactory managerFactory = Persistence.createEntityManagerFactory("ConexionJPA");
        EntityManager entityManager = managerFactory.createEntityManager();
        CriteriaBuilder criteriaBuilder = entityManager.getCriteriaBuilder();
        CriteriaQuery<Videojuegos> criteriaQuery = criteriaBuilder.createQuery(Videojuegos.class);
        Root<Videojuegos> raizVideojuegos = criteriaQuery.from(Videojuegos.class);

        criteriaQuery.select(raizVideojuegos);
        criteriaQuery.orderBy(criteriaBuilder.asc(raizVideojuegos.get("nombre")));
